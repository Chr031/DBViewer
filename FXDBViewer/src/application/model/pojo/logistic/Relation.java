package application.model.pojo.logistic;

public class Relation {

	private Address start;
	private Address end;

	public Relation() {

	}

	public Address getStart() {
		return start;
	}

	public void setStart(Address start) {
		this.start = start;
	}

	public Address getEnd() {
		return end;
	}

	public void setEnd(Address end) {
		this.end = end;
	}
}
